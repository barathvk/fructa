package com.xing.coding.challenge.security;

public class User {
  private String id;
  private String password;

  String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  String getPassword() {
    return password;
  }

  public void setPassword(String password) {
    this.password = password;
  }
}
